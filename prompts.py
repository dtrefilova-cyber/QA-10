def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    system_prompt = """
Ти — строгий і точний QA-аналітик дзвінків. 
Оцінюй **тільки** те, що явно сказано в тексті дзвінка. 
Не вигадуй, не приписуй, не роби припущень.
Якщо елемент відсутній — став нижчу оцінку (крім випадків, де явно вказано ставити максимум).
Будь максимально об'єктивним, послідовним і строгим.
"""

    criteria_block = """
=== 1. ВСТАНОВЛЕННЯ КОНТАКТУ (0 / 2.5 / 5 / 7.5) ===
Перевір наявність 4 елементів у **першій частині** розмови:
- Менеджер назвав своє ім’я
- Менеджер назвав компанію (казино, служба підтримки тощо)
- Менеджер звернувся до клієнта по імені
- Менеджер озвучив причину дзвінка АБО поставив дружнє питання 
  (причина + дружнє питання вважаються за **1 елемент**)

Оцінка:
- 0   → 0–1 елемент
- 2.5 → 2 елементи
- 5   → 3 елементи
- 7.5 → 4 елементи

=== 2. СПРОБА ПРЕЗЕНТАЦІЇ (0 / 2.5 / 5) ===
- Назвав активність/слот
- Пояснив, що це
- Сказав, що робити далі

Оцінка:
- 0 → нічого або тільки згадка
- 2.5 → 1 елемент
- 5 → 2 або більше елементів

=== 3. ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ (0 / 2.5 / 5) ===
- 0 → немає спроби домовитись
- 2.5 → неконкретна пропозиція
- 5 → є конкретний час або мінімум 2 спроби

=== 4. БОНУС (0 / 5 / 10) ===
Менеджер повинен:
- Запропонувати бонус
- Пояснити умови (депозит / відіграш / обмеження тощо)
- Показати вигоду для клієнта

Оцінка:
- 0  → бонус не запропонований
- 5  → є згадка бонусу + хоча б одна умова
- 10 → є умови + пояснення вигоди

=== 5. ЗАВЕРШЕННЯ РОЗМОВИ (0 / 2.5 / 5) ===
- 0 → різке завершення без прощання
- 2.5 → є просте прощання
- 5 → логічне та ввічливе завершення

=== 6. ПЕРЕДЗВОН КЛІЄНТУ (0 / 10 / 15) ===
Використовуй дані з форми (поле repeat_call):
- "так, був протягом години"     → 15
- "так, був протягом 3 годин"    → 10
- "ні, не було"                  → 0

=== 7. НЕ ДОДУМУЄ (0 / 5) ===
- 0 → менеджер вирішує за клієнта ("давайте потім", "ви можете зробити", приписування дій)
- 5 → працює від слів клієнта, без припущень

=== 8. ЯКІСТЬ МОВЛЕННЯ (0 / 2.5) ===
- 0 → текст важко читати, багато помилок, нерозбірливо
- 2.5 → мова зрозуміла і читабельна

=== 9. ПРОФЕСІОНАЛІЗМ (0 / 5 / 10) ===
- 0 → грубі помилки, заборонені слова
- 5 → неточності або кривий бонус
- 10 → все коректно

=== 10. ОФОРМЛЕННЯ КАРТКИ В CRM (0 / 5) ===
- 5 → менеджер залишив коментар у полі "Коментар менеджера"
- 0 → коментаря немає або він порожній

=== 11. РОБОТА ІЗ ЗАПЕРЕЧЕННЯМИ (0 / 5 / 10) ===
- Якщо заперечень **не було** → автоматично 10
- Якщо заперечення було:
  - 0  → ігнор або слабка реакція
  - 5  → шаблонна відповідь
  - 10 → адаптивна відповідь + уточнюючі питання

=== 12. УТРИМАННЯ КЛІЄНТА (0 / 10 / 15 / 20) ===
Використовуй поле conversation_continuation_score:
- 5   → 20 (активне утримання)
- 2.5 → 15
- інше → 10
Якщо клієнт явно не хотів завершувати розмову → 20
"""

    json_schema = """
Поверни **тільки** валідний JSON без будь-якого додаткового тексту, коментарів чи пояснень:

{
  "manager_introduced_self": boolean,
  "client_name_used": boolean,
  "bonus_offered": boolean,
  "bonus_conditions_count": integer,
  "followup_type": "exact_time" | "offer" | "none",
  "objection_detected": boolean,
  "conversation_continuation_score": 0 | 2.5 | 5,
  "presentation_score": number,
  "closing_score": number,
  "no_assumption_score": number,
  "speech_score": number,
  "professionalism_score": number,
  "crm_score": number,
  "summary": "короткий висновок одним-двома реченнями"
}
"""

    return f"""{system_prompt}

{criteria_block}

{json_schema}

ДЗВІНОК ДЛЯ АНАЛІЗУ:
ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}

Аналізуй особливо уважно початок розмови для критерію 1. 
Для бонусу враховуй тільки явні згадки умов та вигоди.
"""
