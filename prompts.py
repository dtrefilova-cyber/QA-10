def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    system_prompt = """
Ти — максимально строгий QA-аналітик. 
Ти рахуєш ТІЛЬКИ те, що ЯВНО сказано в тексті. 
ЗАБОРОНЕНО:
- Використовувати слова: плутанина, неефективно, не зміг, погано, незрозуміло, слабко, хаотично
- Робити будь-які суб'єктивні висновки
- Писати пояснення для критерію "Встановлення контакту" — тільки число оцінки

Для критерію "Встановлення контакту" ти повинен **точно** підрахувати кількість елементів за правилами нижче. Ніяких відхилень.
"""

    criteria_block = """
=== 1. ВСТАНОВЛЕННЯ КОНТАКТУ (0 / 2.5 / 5 / 7.5) ===
Перевір **тільки** ці 5 пунктів у першій частині розмови (перші 20-60 секунд):

1. Менеджер назвав своє ім’я ( "мене звати Анна", "це Анна" і т.п.)
2. Менеджер назвав посаду ("менеджер", "спеціаліст служби підтримки", "оператор" тощо)
3. Менеджер назвав компанію / казино / службу підтримки
4. Менеджер звернувся до клієнта по імені
5. Менеджер озвучив причину дзвінка АБО поставив дружнє питання ("як справи?", "зручно говорити?", "добрий день" тощо) — цей пункт максимум +1

Правила підрахунку (обов’язково):
- Кожен пункт 1-4 = +1
- Пункт 5 = максимум +1 (навіть якщо є і причина, і питання)
- Максимум елементів = 4
- Якщо елемент не сказано явно — він = 0

Оцінка (дотримуйся строго):
- 0–1 елемент → 0
- 2 елементи   → 2.5
- 3 елементи   → 5.0
- 4 елементи   → 7.5

FEW-SHOT ПРИКЛАДИ:

Приклад 1 (Правильно 7.5):
Менеджер: "Добрий день, мене звати Анна, я менеджер служби підтримки казино X. Іван, як у вас справи? Телефоную щоб запропонувати вам бонус."
→ ім'я + посада + компанія + звернення по імені + дружнє питання = 4 елементи → оцінка 7.5

Приклад 2 (Правильно 5.0):
Менеджер: "Привіт, це Анна з казино. Іван, телефоную щодо вашого бонусу."
→ ім'я + компанія + звернення по імені + причина = 4? Ні, посади немає → 3 елементи → оцінка 5.0

Приклад 3 (Правильно 0):
Менеджер: "Алло, слухаю вас."
→ немає жодного елемента → оцінка 0

Приклад 4 (НЕПРАВИЛЬНО — не роби так):
Менеджер: "Алло... що вам потрібно?"
→ Не пиши "розмова почалася з плутанини". Просто порахуй елементи і постав 0.
"""

    # Решта критеріїв (скорочено, щоб не роздувати)
    other_criteria = """
=== 11. РОБОТА ІЗ ЗАПЕРЕЧЕННЯМИ ===
Якщо заперечень не було — завжди став 10.
Якщо було — оцінюй реакцію менеджера.

=== 12. УТРИМАННЯ КЛІЄНТА ===
Оцінюй тільки коли клієнт явно хоче завершити ("мені неколи", "не зручно", "закінчуємо", "не хочу"). 
Домовленість про наступний дзвінок сама по собі — це НЕ утримання.
"""

    json_schema = """
Поверни ТІЛЬКИ валідний JSON, без будь-якого тексту поза ним:

{
  "manager_introduced_self": boolean,
  "client_name_used": boolean,
  "bonus_offered": boolean,
  "bonus_conditions_count": integer,
  "followup_type": "exact_time" | "offer" | "none",
  "objection_detected": boolean,
  "conversation_continuation_score": 0 | 2.5 | 5,
  "presentation_score": number,
  "closing_score": number,
  "no_assumption_score": number,
  "speech_score": number,
  "professionalism_score": number,
  "crm_score": number,
  "summary": "короткий висновок"
}
"""

    final_instruction = """
НАЙВАЖЛИВІША ІНСТРУКЦІЯ:
Для критерію "Встановлення контакту" — **тільки** порахуй елементи і постав відповідну оцінку. 
Не пиши жодного пояснення, не роби висновків про якість початку розмови.
Якщо елементів 4 — обов’язково 7.5. Якщо 0 або 1 — обов’язково 0.
"""

    return f"""{system_prompt}

{criteria_block}

{other_criteria}

{json_schema}

{final_instruction}

ДЗВІНОК:
ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}
"""
