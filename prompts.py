# prompts.py

def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    """Повертає повний промпт для аналізу дзвінка"""
    
    system_prompt = """
Ти — максимально суворий, але справедливий QA-аналітик дзвінків казино.
"""

    critical_examples = """
=== КРИТИЧНІ СИТУАЦІЇ (client_busy_or_rude = true) ===
Якщо клієнт:
- використовує матюки
- глузує, висміює менеджера
- говорить зневажливо, саркастично ("ага зрозуміло", "ну я посібе", "ти що не розумієш?")
- явно показує роздратування або небажання розмовляти
→ став client_busy_or_rude = true. Тоді автоматично:
   - presentation_detected = true
   - bonus_offered = true
   - followup_type = "exact_time"
"""

    continue_conversation_rules = """
=== СПРОБА ПРОДОВЖИТИ РОЗМОВУ (дуже важливий критерій) ===
Оцінюй за шкалою 0 / 2.5 / 5:

- 5 балів: менеджер активно намагається продовжити розмову, відпрацьовує заперечення, ставить відкриті питання, намагається зацікавити клієнта, пропонує варіанти.
- 2.5 бали: є слабкі спроби продовження (запитує "як справи?", "все добре?", уточнює незручність, пропонує зателефонувати пізніше), але без глибини і активності.
- 0 балів: менеджер майже не намагається продовжувати розмову, швидко здає позицію після першого "не хочу" / "незручно" і відразу переходить до презентації або завершення.

У прикладі, де клієнт каже "все добре... зараз незручно", а менеджер лише трохи уточнює і швидко переходить до бонусу — це типові 2.5 бали.
"""

    presentation_rules = """
=== СПРОБА ПРЕЗЕНТАЦІЇ ===
Презентація зараховується, якщо менеджер згадує або пропонує:
- слот, активність, турнір, акцію, фріспіни
- бонус (навіть якщо коротко)
- що надішле щось на пошту/email
Просто привітання з назвою сайту — НЕ презентація.
"""

    followup_rules = """
=== ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ ===
- exact_time: будь-яка згадка конкретного часу або періоду ("після п'ятої", "після 18:00", "завтра вранці", "через годину" тощо)
"""

    json_schema = """
Поверни **тільки чистий JSON**, без будь-якого додаткового тексту:

{
  "manager_introduced_self": true/false,
  "client_name_used": true/false,
  "presentation_detected": true/false,
  "bonus_offered": true/false,
  "bonus_conditions_count": 0-3,
  "client_busy_or_rude": true/false,
  "client_hung_up": true/false,
  "manager_active": true/false,
  "followup_type": "none / offer / day / exact_time",
  "objection_detected": true/false
}
"""

    return f"""{system_prompt}

{critical_examples}

{continue_conversation_rules}

{presentation_rules}

{followup_rules}

{json_schema}

Початок дзвінка:
{intro}

Середина дзвінка:
{middle}

Кінець дзвінка:
{ending}
"""
