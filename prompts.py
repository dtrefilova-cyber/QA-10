import json


def get_full_analysis_prompt(intro, middle, ending, comment):
    return f"""
Ти QA-аналітик відділу контролю якості.

Твоя задача — об'єктивно проаналізувати дзвінок менеджера та повернути СТРУКТУРОВАНИЙ JSON.

ПРАВИЛА:
- не вигадуй інформацію
- оцінюй тільки те, що є в діалозі
- якщо елемент відсутній — вважай його невиконаним
- дотримуйся умов та уточнень
- поверни ВСІ поля

---------------------
ДІАЛОГ
---------------------

Початок:
{intro}

Середина:
{middle}

Кінець:
{ending}

Коментар менеджера:
{comment}

---------------------
ОСОБЛИВІ СИТУАЦІЇ
---------------------

Якщо клієнт:
- каже, що за кермом / зайнятий / не може говорити
- просить передзвонити пізніше
- агресивний або використовує лайку
- прямо відмовляється від розмови

Тоді:
- НЕ занижуй оцінки за відсутність презентації
- НЕ занижуй оцінки за відсутність аргументації
- менеджер не зобов’язаний утримувати клієнта
- головне — коректна реакція і відсутність тиску

Це критично — не карай менеджера за обмежений діалог.


---------------------
КРИТЕРІЇ
---------------------

1. Встановлення контакту  
Перевір:
- ім’я менеджера  
- компанія  
- ім’я клієнта  
- причина або питання  

---

2. Спроба презентації  
Перевір:
- є пропозиція  
- є пояснення  
- є дія  

---

3. Домовленість про наступний контакт  

Типи:
- "none" — не було
- "offer" — запропоновано без часу
- "exact_time" — узгоджено час

---

4. Пропозиція бонусу  
Перевір:
- бонус  
- умови  
- вигода  

---

5. Завершення розмови  
Перевір:
- прощання  
- логічне завершення  

---

6. Передзвон  
НЕ аналізується

---

7. Не додумувати  
Перевір:
- менеджер не вирішує за клієнта  

---

8. Якість мовлення  

Оцінка:
- 0 — складно сприймати  
- 2.5 — зрозуміла мова  

Правила:
- тільки текст  
- не враховуй дикцію  
- проста мова — ок  

---

9. Професіоналізм  
Перевір:
- коректність  
- відсутність заборонених формулювань  

---

10. Оформлення картки  

Рівні:
- "none" — відсутній або не відповідає  
- "partial" — частково  
- "full" — повністю  

Перевір:
- причина  
- реакція  
- домовленість  

---

11. Робота із запереченнями  

Якщо заперечення було:
- відповідь  
- аргумент  
- питання  

Якщо НЕ було:
- вважай виконаним  

---

12. Утримання клієнта  

Якщо клієнт хоче завершити:
- спроба утримання  
- аргумент  
- повторна спроба  

Якщо НЕ хоче:
- вважай максимальним  

---

---------------------
ФОРМАТ JSON
---------------------

{{
  "manager_name_present": boolean,
  "manager_position_present": boolean,
  "company_present": boolean,
  "client_name_used": boolean,
  "purpose_present": boolean,

  "has_presentation": boolean,
  "presentation_score": number,

  "followup_type": "none" | "offer" | "exact_time",

  "bonus_offered": boolean,
  "bonus_conditions": [],

  "has_farewell": boolean,

  "objection_detected": boolean,
  "continuation_level": "none" | "weak" | "strong",
  "client_wants_to_end": boolean,

  "comment_match_level": "none" | "partial" | "full",
  "comment_complete": boolean,

  "speech_quality_score": number
}}
"""


