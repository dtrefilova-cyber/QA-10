# prompts.py

def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    """Повертає повний промпт для аналізу дзвінка"""
    
    system_prompt = """
Ти — справедливий, уважний і точний QA-аналітик дзвінків казино. 
Оцінюй роботу менеджера строго за правилами, які я надаю нижче.
"""

    continuation_rules = """
=== СПРОБА ПРОДОВЖИТИ РОЗМОВУ (оцінка 0 / 2.5 / 5) ===
Оцінюй за шкалою:

- **5 балів**: Менеджер активно намагається затримати клієнта, ставить кілька запитань, пропонує варіанти, розмова продовжується.
- **2.5 бали**: Менеджер робить одну формальну спробу, але швидко погоджується завершити розмову.
- **0 балів**: Менеджер не намагається затримати клієнта, одразу погоджується передзвонити або завершує розмову.

ВАЖЛИВІ ПРИКЛАДИ:
1. Клієнт: "я зайнята, можна передзвонити?"
   Менеджер: "так, давайте завтра після п'ятої" → **0 балів** (немає спроби затримати)

2. Клієнт: "я зайнята, можна передзвонити?"
   Менеджер: "розумію, це займе хвилинку, давайте швидко обговоримо" → **5 балів**

3. Клієнт: "незручно"
   Менеджер: "давайте я бонус залишу" → **2.5 бали** (спроба є, але слабка)
"""

    critical_examples = """
=== КРИТИЧНІ СИТУАЦІЇ (client_busy_or_rude = true) ===
Тільки якщо клієнт явно грубить, матюкається, глузує або зневажливо висміює менеджера.
Не став true просто через те, що клієнт каже "не цікаво" або "незручно".
"""

    presentation_rules = """
=== СПРОБА ПРЕЗЕНТАЦІЇ (оцінка 0 / 2.5 / 5) ===
Оцінюй за шкалою:

- **5 балів**: Менеджер назвав конкретний слот, турнір, активність з сайту та дає коротку інформацію про нього.
- **2.5 бали**: Менеджер згадує про бонус, акцію, але без пояснення, або просто каже "я вам бонус залишу".
- **0 балів**: Менеджер не презентував жодного інфоприводу чи слота з сайту.

ВАЖЛИВІ ПРИКЛАДИ:
1. Менеджер: "я вам бонус залишу" → **2.5 бали** (згадка про бонус, але без презентації)
2. Менеджер: "я вам бонус залишу" + опис бонусу (без згадки слота/активності) → **2.5 бали**
3. Менеджер: "у нас зараз турнір на слоті Book of Dead" → **5 балів**
4. Менеджер: "я вам надішлю пропозицію на email" → **2.5 бали**
5. Менеджер не згадує нічого про бонуси, слоти, активності → **0 балів**
"""

    followup_rules = """
=== ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ ===
- exact_time — будь-яка згадка конкретного часу ("після п'ятої", "після 18:00", "завтра вранці" тощо)
- day — згадка дня, але без конкретного часу ("завтра", "наступного тижня")
- offer — пропозиція зателефонувати, але без конкретики ("передзвоню", "наберу пізніше")
- none — немає домовленості
"""

    objection_rules = """
=== ВИЯВЛЕННЯ ЗАПЕРЕЧЕНЬ (objection_detected) ===
Познач objection_detected = true, якщо клієнт прямо або опосередковано висловлює небажання продовжувати розмову:
- "незручно"
- "не цікаво"
- "не хочу"
- "потім"
- "я зайнятий"
- "можна передзвонити"

Якщо клієнт спокійно сприймає розмову і не висловлює заперечень — став false.
"""

    json_schema = """
Поверни **тільки чистий JSON**, без будь-яких пояснень, без markdown, без додаткового тексту:

{
  "manager_introduced_self": true/false,
  "client_name_used": true/false,
  "presentation_score": 0/2.5/5,
  "bonus_offered": true/false,
  "bonus_conditions_count": 0-3,
  "client_busy_or_rude": true/false,
  "client_hung_up": true/false,
  "manager_active": true/false,
  "followup_type": "none / offer / day / exact_time",
  "objection_detected": true/false,
  "conversation_continuation_score": 0/2.5/5
}
"""

    return f"""{system_prompt}

{continuation_rules}

{critical_examples}

{presentation_rules}

{followup_rules}

{objection_rules}

{json_schema}

Початок дзвінка:
{intro}

Середина дзвінка:
{middle}

Кінець дзвінка:
{ending}
"""
