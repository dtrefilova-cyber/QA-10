def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    system_prompt = """
Ти — максимально строгий і точний QA-аналітик дзвінків.
Твоя головна задача — рахувати тільки те, що **явно сказано** в тексті.
ЗАБОРОНЕНО:
- Використовувати слова: "плутанина", "неефективно", "не зміг", "погано", "незрозуміло", "слабко"
- Робити загальні висновки про якість початку розмови
- Приписувати менеджеру те, чого немає в тексті
"""

    criteria_block = """
=== 1. ВСТАНОВЛЕННЯ КОНТАКТУ (0 / 2.5 / 5 / 7.5) ===
Перевір **строго** наявність цих елементів у першій частині розмови:
1. Менеджер назвав своє ім’я
2. Менеджер назвав посаду
3. Менеджер назвав компанію/казино/службу підтримки
4. Менеджер звернувся до клієнта по імені
5. Менеджер озвучив мету дзвінка АБО поставив дружнє питання — максимум +1 елемент

Оцінка:
- 0-1 елемент → 0
- 2 елементи → 2.5
- 3 елементи → 5.0
- 4 елементи → 7.5
"""

    rest_criteria = """
=== 2. СПРОБА ПРЕЗЕНТАЦІЇ (0 / 5 / 10) ===
Презентація означає тільки опис продукту, слоту, гри чи активності.
Не рахувати як презентацію:
- називання мети дзвінка
- представлення себе чи компанії
- згадку про депозит, якщо вона стосується лише бонусу
- будь-яку пропозицію бонусу (навіть якщо згадуються умови бонусу) - це не презентація
- будь-яку згадку слова "акція" у контексті бонусів — це не презентація

=== 3. ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ (0 / 2.5 / 5) ===
- Точний час → 5
- Лише пропозиція → 2.5
- Нічого → 0

=== 4. ПРОПОЗИЦІЯ БОНУСУ (0 / 5 / 10) ===
- Не запропоновано → 0
- Є 1 умова → 5
- Є ≥2 умови → 10

=== 5. ЗАВЕРШЕННЯ РОЗМОВИ (0 / 2.5 / 5) ===
- Якщо завершення ввічливе і структуроване → 5
- Якщо частково → 2.5
- Якщо відсутнє → 0

=== 6. ПЕРЕДЗВОН КЛІЄНТУ (0 / 10 / 15) ===
- Якщо повторний дзвінок протягом години → 15
- Якщо протягом 2 годин → 10
- Якщо не було → 0

=== 7. НЕ ДОДУМУЄ (0 / 2.5 / 5) ===
- 0 → якщо менеджер робить припущення або підштовхує клієнта до того, що йому незручно (наприклад, питає "чи є час на розмову", "чи є пару хвилин")
- 2.5 → якщо частково дотримується правила
- 5 → якщо менеджер не робить припущень і не підштовхує клієнта

=== 8. ЯКІСТЬ МОВЛЕННЯ (0 / 2.5) ===
Оцінка за ручною перевіркою QA.

=== 9. ПРОФЕСІОНАЛІЗМ (0 / 5 / 10) ===
- Якщо менеджер вживає заборонені слова → 0
- Якщо бонус нараховано помилково → 5
- Інакше → 10

Заборонені слова: Лотерея, Акція, Розіграш, Реклама, Подарунок, Популяризація, Лотерейний білет, Даруємо, Розігруємо, Конкурс, Кешбек, Відшкодуємо, Компенсація, Повернення, Фріспіни, Безкоштовно, Страхування, страховка, ставка без ризику, фрібет, Бездеп.

=== 10. ОФОРМЛЕННЯ КАРТКИ В CRM (0 / 2.5 / 5) ===
- Якщо коментар співпадає зі змістом розмови → 5
- Якщо відображає ~30% суті (бонус, час повторної комунікації тощо) → 2.5
- Якщо не відображає суть → 0

=== 11. РОБОТА ІЗ ЗАПЕРЕЧЕННЯМИ (0 / 5 / 10) ===
- Якщо заперечень не було → 10
- Якщо були:
  - менеджер впевнено продовжив → 10
  - частково → 5
  - не впорався → 0

=== 12. УТРИМАННЯ КЛІЄНТА (0 / 10 / 15 / 20) ===
- Якщо менеджер був пасивним, не робив спроб утримати клієнта або спеціально завершив розмову → 0
- Якщо утримав слабко → 10
- Якщо частково → 15
- Якщо впевнено утримав → 20
"""

    json_schema = """
Поверни **тільки** валідний JSON:

{
  "manager_introduced_self": boolean,
  "client_name_used": boolean,
  "bonus_offered": boolean,
  "bonus_conditions_count": integer,
  "followup_type": "exact_time" | "offer" | "none",
  "objection_detected": boolean,
  "conversation_continuation_score": 0 | 2.5 | 5,
  "presentation_score": number,
  "closing_score": number,
  "no_assumption_score": number,
  "speech_score": number,
  "professionalism_score": number,
  "crm_score": number,
  "summary": "короткий висновок 1 реченням"
}
"""

    final_instruction = """
НАЙВАЖЛИВІШЕ:
- Для критерію 1 рахувати тільки кількість елементів за правилами.
- Для критерію 2 враховувати тільки реальну презентацію продукту/слоту/активності, не мету дзвінка і не депозит при бонусі.
- Для критерію 5 оцінка тільки 0, 2.5 або 5.
- Для критерію 6 враховувати повторний дзвінок протягом 1 або 2 годин.
- Для критерію 7 враховувати, чи менеджер не підштовхує клієнта до незручності.
- Для критерію 9 перевіряти заборонені слова.
- Для критерію 10 оцінювати якість коментаря відповідно до змісту розмови.
- Для критерію 12 можливий 0 балів, якщо менеджер був пасивним і спеціально завершив розмову.
"""

    return f"""{system_prompt}

{criteria_block}

{rest_criteria}

{json_schema}

{final_instruction}

ДЗВІНОК ДЛЯ АНАЛІЗУ:
ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}
"""
