def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    system_prompt = """
Ти — максимально строгий і точний QA-аналітик дзвінків.
Твоя головна задача — рахувати ТІЛЬКИ те, що **явно сказано** в тексті.
ЗАБОРОНЕНО:
- Використовувати слова: "плутанина", "неефективно", "не зміг", "погано", "незрозуміло", "слабко", "повноцінна"
- Робити загальні висновки про якість
- Приписувати менеджеру те, чого немає в тексті
- Зараховувати згадку бонусу як презентацію продукту
"""

    criteria_block = """
=== 1. ВСТАНОВЛЕННЯ КОНТАКТУ (0 / 2.5 / 5 / 7.5) ===
Перевір **строго** наявність цих елементів у першій частині розмови:
1. Менеджер назвав своє ім’я
2. Менеджер назвав посаду (менеджер, оператор, спеціаліст тощо)
3. Менеджер назвав компанію/казино/службу підтримки
4. Менеджер звернувся до клієнта по імені
5. Менеджер озвучив мету дзвінка АБО поставив дружнє питання — максимум +1 елемент

Оцінка:
- 0-1 елемент → 0
- 2 елементи → 2.5
- 3 елементи → 5.0
- 4 елементи → 7.5

=== 2. СПРОБА ПРЕЗЕНТАЦІЇ (0 / 5 / 10) ===
Презентація = **тільки** опис продукту, слоту, гри чи активності (наприклад: опис конкретного слоту, правил гри, особливостей турніру).

ЗАБОРОНЕНО зараховувати як презентацію:
- Згадку мети дзвінка ("телефоную щоб...")
- Представлення себе або компанії
- Будь-яку згадку бонусу, акції, депозиту, фріспінів
- Слова "акція", "бонус", "пропозиція" в контексті грошей

Оцінка:
- 0  → презентації продукту/слоту/гри не було
- 5  → була лише згадка назви слоту/гри без опису
- 10 → була реальна презентація (назва + опис того, що це таке або як грати)

=== 3. ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ (0 / 2.5 / 5) ===
- Точний час → 5
- Лише пропозиція без конкретики → 2.5
- Нічого → 0

=== 4. ПРОПОЗИЦІЯ БОНУСУ (0 / 5 / 10) ===
- Не запропоновано → 0
- Запропоновано + 1 умова (депозит, відіграш, обмеження тощо) → 5
- Запропоновано + 2 і більше умов → 10

=== 5. ЗАВЕРШЕННЯ РОЗМОВИ (0 / 2.5 / 5) ===
- Ввічливе і структуроване завершення → 5
- Просте прощання → 2.5
- Різке завершення → 0

=== 6. ПЕРЕДЗВОН КЛІЄНТУ (0 / 10 / 15) ===
- "так, був протягом години" → 15
- "так, був протягом 2 годин" → 10
- "ні, не було" → 0

=== 7. НЕ ДОДУМУЄ (0 / 2.5 / 5) ===
- 0 → менеджер підштовхує клієнта або робить припущення ("чи є у вас час?", "чи маєте пару хвилин?")
- 5 → менеджер працює тільки від слів клієнта, без припущень

=== 8. ЯКІСТЬ МОВЛЕННЯ (0 / 2.5) ===
Оцінка за ручною перевіркою QA.

=== 9. ПРОФЕСІОНАЛІЗМ (0 / 5 / 10) ===
- Використав заборонені слова → 0
- Бонус нараховано помилково → 5
- Інакше → 10

=== 10. ОФОРМЛЕННЯ КАРТКИ В CRM (0 / 2.5 / 5) ===
- Коментар добре відображає суть розмови → 5
- Коментар частково відображає суть → 2.5
- Коментар відсутній або не відображає суть → 0

=== 11. РОБОТА ІЗ ЗАПЕРЕЧЕННЯМИ (0 / 5 / 10) ===
- Заперечень не було → 10
- Заперечення було + добра реакція → 10
- Заперечення було + слабка реакція → 5
- Заперечення було + ігнор → 0

=== 12. УТРИМАННЯ КЛІЄНТА (0 / 10 / 15 / 20) ===
Оцінюй тільки коли клієнт явно хотів завершити розмову.
- Активне утримання з аргументами → 20
- Слабка спроба → 15
- Пасивна поведінка або сам завершив → 10 або нижче
"""

    json_schema = """
Поверни **тільки** валідний JSON без будь-якого додаткового тексту:

{
  "manager_introduced_self": boolean,
  "client_name_used": boolean,
  "bonus_offered": boolean,
  "bonus_conditions_count": integer,
  "followup_type": "exact_time" | "offer" | "none",
  "objection_detected": boolean,
  "conversation_continuation_score": 0 | 2.5 | 5,
  "presentation_score": number,
  "closing_score": number,
  "no_assumption_score": number,
  "speech_score": number,
  "professionalism_score": number,
  "crm_score": number,
  "summary": "короткий висновок 1 реченням"
}
"""

    final_instruction = """
НАЙВАЖЛИВІШІ ПРАВИЛА:
- Критерій 2 (Спроба презентації): НІКОЛИ не зараховувати згадку бонусу, акції чи депозиту як презентацію продукту.
- Критерій 4 (Пропозиція бонусу): можливі ТІЛЬКИ оцінки 0, 5 або 10. Не став 2.5.
- Для критерію 1 рахувати тільки кількість елементів.
- Будь максимально строгим і об'єктивним.
"""

    return f"""{system_prompt}

{criteria_block}

{json_schema}

{final_instruction}

ДЗВІНОК ДЛЯ АНАЛІЗУ:
ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}
"""
