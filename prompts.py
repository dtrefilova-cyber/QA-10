# prompts.py

def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    """Повертає повний промпт для аналізу дзвінка"""
    
    system_prompt = """
Ти — справедливий, уважний і точний QA-аналітик дзвінків казино. 
Оцінюй роботу менеджера строго за правилами, які я надаю нижче.
"""

    continuation_rules = """
=== СПРОБА ПРОДОВЖИТИ РОЗМОВУ (оцінка 0 / 2.5 / 5) ===
Оцінюй за шкалою:

- **5 балів**: Менеджер спробував затримати клієнта у розмові, і розмова продовжилась. Активно відпрацьовує заперечення, ставить відкриті питання, пропонує варіанти.
- **2.5 бали**: Присутня спроба затримати клієнта у розмові, але не доведена до кінця. Наприклад: менеджер реагує на "незручно" фразою "давайте я бонус залишу" і не робить подальших спроб продовжити спілкування.
- **0 балів**: Менеджер не спробував затримати клієнта та продовжити розмову після першого сигналу ("не хочу", "незручно", "все добре").
"""

    critical_examples = """
=== КРИТИЧНІ СИТУАЦІЇ (client_busy_or_rude = true) ===
Тільки якщо клієнт явно грубить, матюкається, глузує або зневажливо висміює менеджера.
Не став true просто через те, що клієнт каже "не цікаво" або "незручно".
"""

    presentation_rules = """
=== СПРОБА ПРЕЗЕНТАЦІЇ (оцінка 0 / 2.5 / 5) ===
Оцінюй за шкалою:

- **5 балів**: Менеджер назвав інфопривід або слот та дає коротку інформацію про нього. Конкретна пропозиція з поясненням.
- **2.5 бали**: Менеджер згадував про інфопривід чи слот з сайту, але без пояснення. Наприклад: "стосовно бонусу", "щодо акції", "про активність".
- **0 балів**: Менеджер не презентував жодного інфоприводу чи слота з сайту.
"""

    followup_rules = """
=== ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ ===
- exact_time — будь-яка згадка конкретного часу ("після п'ятої", "після 18:00", "завтра вранці" тощо)
"""

    json_schema = """
Поверни **тільки чистий JSON**, без будь-яких пояснень, без markdown, без додаткового тексту:

{
  "manager_introduced_self": true/false,
  "client_name_used": true/false,
  "presentation_score": 0/2.5/5,
  "bonus_offered": true/false,
  "bonus_conditions_count": 0-3,
  "client_busy_or_rude": true/false,
  "client_hung_up": true/false,
  "manager_active": true/false,
  "followup_type": "none / offer / day / exact_time",
  "objection_detected": true/false,
  "conversation_continuation_score": 0/2.5/5
}
"""

    return f"""{system_prompt}

{continuation_rules}

{critical_examples}

{presentation_rules}

{followup_rules}

{json_schema}

Початок дзвінка:
{intro}

Середина дзвінка:
{middle}

Кінець дзвінка:
{ending}
"""
