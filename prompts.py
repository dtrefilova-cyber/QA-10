# prompts.py

def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    """Повертає повний промпт для аналізу дзвінка"""
    
    system_prompt = """
Ти — справедливий, уважний і точний QA-аналітик дзвінків казино. 
Оцінюй роботу менеджера строго за правилами, які я надаю нижче. Не автоматично зараховуй критерії.
"""

    continue_conversation_rules = """
=== СПРОБА ПРОДОВЖИТИ РОЗМОВУ (дуже важливий критерій) ===
Оцінюй строго за шкалою 0 / 2.5 / 5:

- **5 балів**: Менеджер активно відпрацьовує заперечення клієнта, ставить кілька відкритих питань, намагається зацікавити, пропонує варіанти продовження розмови і не здається швидко.
- **2.5 бали**: Є тільки слабкі, формальні спроби (наприклад: "як справи?", "все добре?", "незручно правильно розумію?"). Менеджер швидко переходить до презентації бонусу або завершення.
- **0 балів**: Менеджер майже не намагається продовжувати розмову після першого сигналу клієнта ("не хочу", "незручно", "все добре").

Увага: Якщо менеджер після "незручно" майже відразу каже "давайте я бонус залишу" — це **2.5 бали**, а НЕ 5.
"""

    critical_examples = """
=== КРИТИЧНІ СИТУАЦІЇ (client_busy_or_rude = true) ===
Тільки якщо клієнт явно грубить, матюкається, глузує або зневажливо висміює менеджера.
Не став true просто через те, що клієнт каже "не цікаво" або "незручно".
"""

    presentation_rules = """
=== СПРОБА ПРЕЗЕНТАЦІЇ ===
Зараховується ТІЛЬКИ якщо менеджер явно пропонує:
- конкретний слот, активність, турнір, акцію, фріспіни
- або детально розповідає про бонус (не просто слово "бонус")

Просто сказати "я бонус залишу" або "стосовно бонусу" — це **слабка** спроба, але все ж зараховується як презентація (5 балів).
Якщо менеджер не розкриває жодної пропозиції — став false.
"""

    followup_rules = """
=== ДОМОВЛЕНІСТЬ ПРО НАСТУПНИЙ КОНТАКТ ===
- exact_time — будь-яка згадка конкретного часу ("після п'ятої", "після 18:00", "завтра вранці" тощо)
"""

    json_schema = """
Поверни **тільки чистий JSON**, без будь-яких пояснень, без markdown, без додаткового тексту:

{
  "manager_introduced_self": true/false,
  "client_name_used": true/false,
  "presentation_detected": true/false,
  "bonus_offered": true/false,
  "bonus_conditions_count": 0-3,
  "client_busy_or_rude": true/false,
  "client_hung_up": true/false,
  "manager_active": true/false,
  "followup_type": "none / offer / day / exact_time",
  "objection_detected": true/false
}
"""

    return f"""{system_prompt}

{continue_conversation_rules}

{critical_examples}

{presentation_rules}

{followup_rules}

{json_schema}

Початок дзвінка:
{intro}

Середина дзвінка:
{middle}

Кінець дзвінка:
{ending}
"""
