def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    system_prompt = """
Ти — максимально строгий і точний QA-аналітик дзвінків.
Твоя головна задача — рахувати тільки те, що **явно сказано** в тексті.
ЗАБОРОНЕНО:
- Використовувати слова: "плутанина", "неефективно", "не зміг", "погано", "незрозуміло", "слабко"
- Робити загальні висновки про якість початку розмови
- Приписувати менеджеру те, чого немає в тексті

Для критерію "Встановлення контакту" ти повинен **тільки** підрахувати кількість елементів за чіткими правилами. Ніяких інших інтерпретацій.
"""

    criteria_block = """
=== 1. ВСТАНОВЛЕННЯ КОНТАКТУ (0 / 2.5 / 5 / 7.5) ===
Перевір **строго** наявність цих 4 елементів у першій частині розмови:

1. Менеджер назвав своє ім’я (наприклад: "мене звати Анна", "це Анна")
2. Менеджер назвав посаду (менеджер, спеціаліст, оператор, співробітник служби підтримки тощо)
3. Менеджер назвав компанію/казино/службу підтримки
4. Менеджер звернувся до клієнта по імені
5. Менеджер озвучив мету дзвінка АБО поставив дружнє питання ("як справи?", "зручно говорити?", "добрий день") — цей пункт = максимум 1 елемент

Правила підрахунку (обов’язково дотримуватися):
- Кожен елемент з 1-4 = +1 бал
- Пункт 5 = максимум +1 (навіть якщо є і причина, і дружнє питання)
- Якщо елемент не сказано явно — він НЕ рахується
- Максимальна кількість елементів = 4

Оцінка:
- 0-1 елемент → 0
- 2 елементи   → 2.5
- 3 елементи   → 5.0
- 4 елементи   → 7.5

ПРИКЛАДИ:
Приклад 1 (7.5):
"Добрий день, мене звати Анна, я менеджер служби підтримки казино X. Іван, як справи? Телефоную щоб запропонувати вам бонус."
→ ім'я + посада + компанія + звернення по імені + дружнє питання = 4 елементи → 7.5

Приклад 2 (2.5):
"Алло, це служба підтримки. Що вам потрібно?"
→ тільки компанія → 1 елемент → 0 (або 2.5 якщо ще щось є)

Приклад 3 (5.0):
"Привіт, це Анна з казино. Іван, телефоную щодо вашого бонусу."
→ ім'я + компанія + звернення по імені + причина = 4? Ні, посади немає → 3 елементи → 5.0
"""

    # Решта критеріїв (2-12) — можна залишити як у попередній версії, або використати спрощену
    rest_criteria = """
=== 2-10. ІНШІ КРИТЕРІЇ ===
(залиш без змін, або скопіюй з попередньої версії)

=== 11. РОБОТА ІЗ ЗАПЕРЕЧЕННЯМИ ===
Якщо заперечень не було — завжди 10.
Якщо було — оцінюй тільки реакцію менеджера.

=== 12. УТРИМАННЯ КЛІЄНТА ===
Оцінюй тільки коли клієнт явно хоче завершити розмову ("мені неколи", "не зручно", "закінчуємо", "не хочу").
Якщо клієнт не давав таких сигналів — не став високий бал тільки за те, що домовились про наступний дзвінок.
"""

    json_schema = """
Поверни **тільки** валідний JSON, без будь-якого тексту поза ним:

{
  "manager_introduced_self": boolean,
  "client_name_used": boolean,
  "bonus_offered": boolean,
  "bonus_conditions_count": integer,
  "followup_type": "exact_time" | "offer" | "none",
  "objection_detected": boolean,
  "conversation_continuation_score": 0 | 2.5 | 5,
  "presentation_score": number,
  "closing_score": number,
  "no_assumption_score": number,
  "speech_score": number,
  "professionalism_score": number,
  "crm_score": number,
  "summary": "короткий висновок 1 реченням"
}
"""

    final_instruction = """
НАЙВАЖЛИВІШЕ:
- Для критерію 1 рахувати **тільки** кількість елементів за правилами вище. 
  Не писати жодних інтерпретацій типу "плутанина", "неефективно", "не зміг встановити контакт".
- Якщо елементів 4 — обов’язково став 7.5
- Якщо елементів 0 або 1 — став 0
- Для всіх інших критеріїв — бути максимально об'єктивним.
"""

    return f"""{system_prompt}

{criteria_block}

{rest_criteria}

{json_schema}

{final_instruction}

ДЗВІНОК ДЛЯ АНАЛІЗУ:
ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}
"""
