def get_full_analysis_prompt(intro: str, middle: str, ending: str) -> str:
    system_prompt = """
Ти — точний QA-аналітик дзвінків.
Твоя задача — оцінити дзвінок строго за критеріями нижче.

ВАЖЛИВО:
- Не вигадуй
- Якщо чогось не було — став максимум (якщо це вказано в правилах)
- Оцінюй тільки на основі тексту дзвінка
- Будь строгим і логічним
"""

    criteria_block = """
=== 1. ВСТАНОВЛЕННЯ КОНТАКТУ (0 / 2.5 / 5 / 7.5) ===
Перевір 4 елементи:
- ім’я менеджера
- компанія
- ім’я клієнта
- причина дзвінка АБО дружнє питання

Оцінка:
0 → 0-1 елемент
2.5 → 2 елементи
5 → 3 елементи
7.5 → 4 елементи

---

=== 2. СПРОБА ПРЕЗЕНТАЦІЇ (0 / 2.5 / 5) ===
- назвав активність/слот
- пояснив що це
- сказав що робити

0 → нічого
2.5 → тільки згадка
5 → мінімум 2 елементи

---

=== 3. ДОМОВЛЕНІСТЬ ПРО КОНТАКТ (0 / 2.5 / 5) ===
0 → немає спроби
2.5 → без конкретики
5 → є час або 2 спроби

---

=== 4. БОНУС (0 / 5 / 10) ===
- запропонував
- пояснив умови
- показав вигоду

0 → немає
5 → є згадка + 1 умова
10 → є умови + вигода

---

=== 5. ЗАВЕРШЕННЯ (0 / 2.5 / 5) ===
0 → різке завершення
2.5 → є прощання
5 → логічне завершення

---

=== 6. ПЕРЕДЗВОН (0 / 5 / 10 / 15) ===
0 → не зробив
5 → запізнення ~2 год
10 → ~1 год
15 → вчасно або не було домовленості

---

=== 7. НЕ ДОДУМУЄ (0 / 2.5 / 5) ===
0 → вирішує за клієнта
2.5 → навідні фрази
5 → працює від клієнта

---

=== 8. МОВЛЕННЯ (0 / 2.5) ===
0 → складно читати
2.5 → зрозуміло

---

=== 9. ПРОФЕСІОНАЛІЗМ (0 / 5 / 10) ===
0 → грубі помилки / заборонені слова
5 → неточності або кривий бонус
10 → все коректно

---

=== 10. КОМЕНТАР В CRM (0 / 2.5 / 5) ===
0 → немає / суперечить
2.5 → неповний
5 → відповідає дзвінку

---

=== 11. ЗАПЕРЕЧЕННЯ (0 / 2.5 / 5 / 7.5 / 10) ===
0 → ігнор
2.5 → шаблон
5 → шаблон + питання
7.5 → адаптація
10 → адаптація + питання

якщо не було → 10

---

=== 12. УТРИМАННЯ (0 / 5 / 10 / 15 / 20) ===
0 → сам завершує
5 → ігнорує
10 → формально
15 → є аргумент
20 → активне утримання

якщо клієнт не хоче завершити → 20
"""

    json_schema = """
Поверни тільки JSON:

{
  "contact_score": number,
  "presentation_score": number,
  "followup_score": number,
  "bonus_score": number,
  "closing_score": number,
  "callback_score": number,
  "no_assumption_score": number,
  "speech_score": number,
  "professionalism_score": number,
  "crm_score": number,
  "objection_score": number,
  "retention_score": number,
  "total_score": number,
  "summary": "короткий висновок"
}
"""

    return f"""{system_prompt}

{criteria_block}

{json_schema}

ДЗВІНОК:

ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}
"""
