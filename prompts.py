import json


def get_full_analysis_prompt(intro, middle, ending, comment):
    return f"""
Ти QA-аналітик відділу контролю якості.

Твоя задача — об'єктивно проаналізувати дзвінок менеджера та повернути СТРУКТУРОВАНИЙ JSON.

ПРАВИЛА:
- не вигадуй інформацію
- оцінюй тільки те, що є в діалозі
- якщо елемент відсутній — вважай його невиконаним
- дотримуйся умов та уточнень
- поверни ВСІ поля
- якщо є сумнів у трактуванні — обирай більш нейтральний варіант (none / false / weak)


---------------------
ДІАЛОГ
---------------------

Початок:
{intro}

Середина:
{middle}

Кінець:
{ending}

Коментар менеджера:
{comment}

---------------------
ОСОБЛИВІ СИТУАЦІЇ
---------------------

Якщо клієнт:
- каже, що за кермом / зайнятий / не може говорити
- просить передзвонити пізніше
- агресивний або використовує лайку
- прямо відмовляється від розмови

→ вважай діалог обмеженим

У цьому випадку:
- НЕ занижуй оцінки за відсутність презентації
- НЕ занижуй оцінки за відсутність аргументації
- менеджер не зобов’язаний утримувати клієнта
- головне — коректна реакція і відсутність тиску

Поверни:
"is_limited_dialogue": true

В інших випадках:
"is_limited_dialogue": false


---------------------
КРИТЕРІЇ
---------------------

1. Встановлення контакту  
Перевір:
- ім’я менеджера  
- компанія  
- ім’я клієнта  
- причина або питання  

---

2. Спроба презентації  

Умови перевірки:
Менеджер:
- назвав слот/активність (є пропозиція)
- пояснив, що це
- сказав, що клієнту потрібно зробити

Рівні:
- "none" — пропозиція відсутня
- "partial" — є тільки згадка або часткове пояснення
- "full" — є пропозиція + пояснення + дія

ВАЖЛИВО:
- “подивіться на сайті” без пояснення → "partial"
- якщо немає пропозиції → "none"

Поверни у JSON:
"presentation_level": "none" | "partial" | "full" 

---

3. Домовленість про наступний контакт  

Типи:
- "none" — не було
- "offer" — запропоновано без часу
- "exact_time" — узгоджено час

---

4. Пропозиція бонусу  
Перевір:
- бонус  
- умови  
- вигода  

---

5. Завершення розмови  
Перевір:
- прощання  
- логічне завершення  

---

6. Передзвон  
НЕ аналізується

---

7. Не додумувати

Умови перевірки:
Менеджер не підштовхує клієнта до завершення розмови і не робить висновки за нього.

Помилки:
- “вам зараз незручно?”
- “я вам не заважаю?”
- “давайте іншим разом”
- “ви, мабуть, зайняті”
- будь-які припущення про стан клієнта (спить, на роботі і т.д.)

ВАЖЛИВО:
- це критерій поведінки менеджера
- не залежить від слів клієнта
- якщо менеджер сам веде до завершення → це помилка 

---

8. Якість мовлення  

Умови перевірки:
Оціни зрозумілість мови менеджера по тексту діалогу

Рівні:
- "bad" — складно сприймати (зламані або нелогічні фрази)
- "good" — мова зрозуміла

ВАЖЛИВО:
- оцінюється тільки текст
- дикція не враховується
- проста мова — це нормально

Поверни у JSON:
"speech_quality": "bad" | "good"

---

9. Професіоналізм  
Перевір:
- коректність  
- відсутність заборонених формулювань  

---

10. Оформлення картки  

Рівні:
- "none" — відсутній або не відповідає  
- "partial" — частково  
- "full" — повністю  

Перевір:
- причина  
- реакція  
- домовленість  

---

11. Робота із запереченнями

Умови перевірки:
Оцінюється ТІЛЬКИ робота із запереченнями щодо продукту/гри.

Заперечення:
- немає часу на гру
- немає грошей
- немає бажання / не цікаво
- немає можливості
- немає віддачі / результату

НЕ Є запереченням:
- “немає часу говорити”
- “я зайнятий”
- “я за кермом”
- “передзвоніть”

(це відноситься до утримання клієнта)

Особлива ситуація:
- якщо клієнт військовий → не опрацьовуються заперечення по часу і грошам

Оцінка:
Якщо заперечення було:
Менеджер працює по PREP:
1. відповідь
2. аргумент
3. підкріплення
4. питання

Якщо заперечення не було:
→ вважай критерій виконаним

ВАЖЛИВО:
- якщо клієнт не хоче говорити → це утримання, не заперечення
- якщо клієнт не хоче продукт → це заперечення
- не змішуй ці ситуації

---

12. Утримання клієнта

Умови перевірки:
Оцінюється реакція на бажання клієнта завершити розмову.

Тригери:
- “немає часу говорити”
- “я зайнятий”
- “не можу говорити”
- “передзвоніть”

Менеджер повинен:
- зробити мінімум 1 спробу утримати клієнта
- максимум 2 спроби (не більше)
- запропонувати “хвилинку”, “30 секунд” тощо
- займати активну позицію

Помилки:
- одразу погоджується завершити
- не робить спроб утримати
- пасивна позиція

ДОДАТКОВА ПОМИЛКА:
- менеджер додумує ситуацію (спить, зайнятий, на роботі)
і веде розмову до завершення

Особлива ситуація:
- якщо клієнт не намагався завершити розмову → вважай утримання максимальним 

Поверни:
"continuation_level":

- "strong" — менеджер зробив 2+ спроби утримати і давав аргументи
- "weak" — була 1 спроба без аргументації або слабка спроба
- "none" — не було спроб утримання

---------------------
ФОРМАТ JSON
---------------------

{{
  "manager_name_present": boolean,
  "manager_position_present": boolean,
  "company_present": boolean,
  "client_name_used": boolean,
  "purpose_present": boolean,

  "presentation_level": "none" | "partial" | "full",

  "followup_type": "none" | "offer" | "exact_time",

  "bonus_offered": boolean,
  "bonus_conditions": array,

  "has_farewell": boolean,
  "is_limited_dialogue": boolean,

  "objection_detected": boolean,
  "continuation_level": "none" | "weak" | "strong",
  "client_wants_to_end": boolean,

  "comment_match_level": "none" | "partial" | "full",
  "comment_complete": boolean,

  "speech_quality": "bad" | "good"
}}
"""


def get_qa_comment_prompt(dialogue, scores):
    return f"""
Ти QA-аналітик.

Дай коментар по кожному критерію.

Правила:
- один критерій = один рядок
- без води
- якщо ок → "виконано"
- якщо ні → коротка причина

Максимальні бали:

{{
  "Встановлення контакту": 7.5,
  "Спроба презентації": 5,
  "Домовленість про наступний контакт": 5,
  "Пропозиція бонусу": 10,
  "Завершення розмови": 5,
  "Передзвон клієнту": 15,
  "Не додумувати": 5,
  "Якість мовлення": 2.5,
  "Професіоналізм": 10,
  "Оформлення картки": 5,
  "Утримання клієнта": 20,
  "Робота із запереченнями": 10
}}

Оцінки:
{json.dumps(scores, ensure_ascii=False)}

Діалог:
{dialogue}
"""
