def get_full_analysis_prompt(intro: str, middle: str, ending: str, comment: str) -> str:
    return f"""
Ти — строгий QA-аналітик дзвінків.
Аналізуєш тільки те, що явно є в тексті. Нічого не вигадуєш.

-------------------------
ОСОБЛИВІ СИТУАЦІЇ
-------------------------
Якщо клієнт:
- грубо спілкується / матюкається
- кидає слухавку
- за кермом / небезпечна ситуація / тривога
- в укритті / тривога
- агресивний / матюкається
- різко завершує розмову

ТОДІ:
- has_presentation = true
- followup_type = "exact_time"
- не занижуй оцінку за відсутність презентації
- якщо була спроба домовитись → followup_type = "exact_time"
- не вважай відсутність утримання помилкою
- заперечення можна не опрацьовувати

Якщо менеджер зробив 2+ спроби домовитись, але клієнт не дав точний час:
→ has_presentation = true
→ followup_type = "exact_time"

-------------------------
1. ВСТАНОВЛЕННЯ КОНТАКТУ
-------------------------
Визнач:
- manager_name_present
- manager_position_present
- company_present
- client_name_used
- purpose_present

-------------------------
2. ПРЕЗЕНТАЦІЯ
-------------------------
has_presentation = true якщо є будь-яка згадка:
- активності
- програми
- гри
- оновлення

has_presentation = true ТІЛЬКИ якщо:
- є назва активності / гри / програми

НАВІТЬ без детального опису

has_presentation = false якщо:
- тільки бонус
- загальні фрази ("подивіться", "є щось")

2.5 — є згадка без пояснення  
5 — є пояснення (що це і що зробити)

-------------------------
3. НАСТУПНИЙ КОНТАКТ
-------------------------
followup_type:

"exact_time":
- "після 16"
- "о 17"
- "з 5 до 6"

"offer":
- "ввечері"
- "потім"

"none":
- якщо не було

-------------------------
4. ПРОПОЗИЦІЯ БОНУСУ
-------------------------
bonus_offered = true, якщо менеджер озвучив бонус

bonus_conditions = список усіх окремих умов або характеристик бонусу

Якщо є 2 або більше різних умов → це >=2 умови

-------------------------
5. ЗАПЕРЕЧЕННЯ
-------------------------
objection_detected якщо клієнт каже:
- немає грошей
- немає часу
- не хоче
- немає віддачі

continuation_level:
- "strong" → є аргументація або рішення
- "weak" → формальна відповідь
- "none" → ігнор

-------------------------
6. УТРИМАННЯ КЛІЄНТА
-------------------------
client_wants_to_end = true якщо клієнт хоче завершити розмову

continuation_level:
- "strong" → активне утримання
- "weak" → слабка реакція
- "none" → немає утримання

-------------------------
7. ЗАВЕРШЕННЯ
-------------------------
has_farewell = true якщо є прощання

-------------------------
10. ОФОРМЛЕННЯ КАРТКИ
-------------------------
Проаналізуй коментар менеджера.

comment_match_level:
- "full"
- "partial"
- "none"

comment_complete = true якщо є:
- причина
- реакція клієнта
- домовленість

-------------------------
ВАЖЛИВО
-------------------------
- не додумуй
- тільки факти з тексту

-------------------------
ФОРМАТ ВІДПОВІДІ
-------------------------
Поверни тільки JSON

{{
  "manager_name_present": false,
  "manager_position_present": false,
  "company_present": false,
  "client_name_used": false,
  "purpose_present": false,
  "bonus_offered": false,
  "bonus_conditions": [],
  "followup_type": "none",
  "objection_detected": false,
  "client_wants_to_end": false,
  "continuation_level": "none",
  "has_presentation": false,
  "has_farewell": false,
  "comment_match_level": "none",
  "comment_complete": false
}}

-------------------------
ДЗВІНОК
-------------------------
ПОЧАТОК:
{intro}

СЕРЕДИНА:
{middle}

КІНЕЦЬ:
{ending}

-------------------------
КОМЕНТАР МЕНЕДЖЕРА
-------------------------
{comment}
"""
